package org.jetbrains.astrid.model

import org.jetbrains.astrid.downloader.Downloader.getPathToBeamModule

class PredictionModel {
    companion object{
        fun buildModel(): ByteArray? {
            val beamSearchPath = getPathToBeamModule()
            return if (beamSearchPath != null) org.tensorflow.TensorFlow.loadLibrary(beamSearchPath) else null
        }
    }
}